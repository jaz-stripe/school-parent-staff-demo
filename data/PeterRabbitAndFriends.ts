// Helper function to get a random character
export function getRandomCharacter() {
  return characters[Math.floor(Math.random() * characters.length)];
}

// Helper function to get random children for a parent
export function getRandomChildren(parentLastName: string, count: number = 2) {
  // Filter children with matching last name
  const matchingChildren = children.filter(child => child.lastName === parentLastName);
  
  // If no matching children, get random ones
  const availableChildren = matchingChildren.length > 0 ? matchingChildren : children;
  
  // Ensure we don't request more children than available
  const actualCount = Math.min(count, availableChildren.length);
  
  // Get random selection
  const selectedChildren = [];
  const indices = new Set();
  
  while (selectedChildren.length < actualCount) {
    const index = Math.floor(Math.random() * availableChildren.length);
    if (!indices.has(index)) {
      indices.add(index);
      selectedChildren.push(availableChildren[index]);
    }
  }
  
  return selectedChildren;
}

// Helper function to get a matching parent for a child by last name
export function getParentForChild(childLastName: string) {
  return characters.find(parent => parent.lastName === childLastName);
}

